    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            Producto p = new Producto();
            p.id = this.producto.id;
            p.existencia = this.producto.existencia;
            p.descripcion = this.inputDescripcion.getText();
            if (p.descripcion.length() <= 0){
                throw new Exception("La descripcion no puede estar vacia.");
            }
            
            
            p.codigo_barras = this.inputCodigoBarras.getText();
            
            if (p.codigo_barras.length() <= 0){
                throw new Exception("El codigo de barras no puede estar vacio.");
            }
            
            if (!Store.isNumeric(p.codigo_barras)){
                throw new Exception("El codigo de barras es invalido.");
            }
            
            String existenciaTxt = this.inputExistencia.getText();
            float existencia = existenciaTxt.length() <= 0 ? 0 : Float.valueOf(existenciaTxt);
            if (Float.isNaN(existencia) || !Float.isFinite(existencia)){
                throw new Exception("La existencia es invalida.");
            }
            
            
            p.existencia = existencia;
            
            if (this.inputCosto.getText().length() <= 0 || Float.valueOf(this.inputCosto.getText()) < 0){
                throw new Exception("El costo es invalido.");
            }
            
            p.costo = Float.valueOf(this.inputCosto.getText());
            
            if (this.inputPrecio.getText().length() <= 0 || Float.valueOf(this.inputPrecio.getText()) < 0){
                throw new Exception("El precio es invalido.");
            }
            
            p.precio = Float.valueOf(this.inputPrecio.getText());
            p.materia_prima = this.checkMateriaPrima.isSelected();
            
            p.save();
            this.dispose();
        }catch(Exception e){
            Store.error("Ha ocurrido un problema", e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

