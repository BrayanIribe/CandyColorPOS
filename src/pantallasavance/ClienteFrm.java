    public void mapFields() {
        this.labelTitle.setText("Actualizar " + modelName);
        this.btnEliminar.setVisible(true);
        if (this.cliente == null || this.cliente.id == 0) {
            this.cliente = new Cliente();
            this.labelTitle.setText("Crear nuevo " + modelName);
            this.btnEliminar.setVisible(false);
            return;
        }
        
        if (this.cliente.rfc.equals("XAXX0101000") && this.cliente.id == 1) {
            this.btnEliminar.setVisible(false);
            this.inputRfc.setEditable(false);
        }
        
        this.inputNombre.setText(this.cliente.nombre);
        this.inputRfc.setText(this.cliente.rfc);
        this.inputReferencia.setText(this.cliente.referencia);
        this.inputTelefono.setText(this.cliente.telefono);
        this.inputCalle.setText(this.cliente.calle);
        this.inputNum.setText(this.cliente.num);
        this.inputColonia.setText(this.cliente.colonia);
        this.inputCodigoPostal.setText(String.valueOf(this.cliente.codigo_postal));
    }

