    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (this.inputCodigoPostal.getText().length() <= 0) {
                this.inputCodigoPostal.setText("85000");
            }
            
            // validar si es publico en general
            
            boolean isPublicoGeneral = this.inputRfc.getText().equalsIgnoreCase("XAXX0101000");
            Vector<Cliente> clientes = Cliente.find("XAXX0101000", esProveedores);
            if (isPublicoGeneral && clientes.get(0).id != this.cliente.id){
                Store.error("Ha ocurrido un problema", "No puede tener mas de un " + modelName + " publico en general.");
                return;
            }

            Cliente c = new Cliente();
            int cp = Integer.valueOf(this.inputCodigoPostal.getText());
            if (cp <= 0 || cp >= 90000) {
                throw new Exception("Codigo postal invalido.");
            }
            c.id = this.cliente.id;
            c.nombre = this.inputNombre.getText();
            if (c.nombre.length() <= 0) {
                throw new Exception("La descripcion no puede estar vacia.");
            }

            c.rfc = this.inputRfc.getText().toUpperCase();
            c.telefono = this.inputTelefono.getText();
            c.referencia = this.inputReferencia.getText();
            c.calle = this.inputCalle.getText();
            c.num = this.inputNum.getText();
            c.colonia = this.inputColonia.getText();
            c.codigo_postal = cp;
            c.es_proveedor = this.esProveedores;

            c.save();
            this.dispose();
        } catch (Exception e) {
            Store.error("Ha ocurrido un problema", e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

